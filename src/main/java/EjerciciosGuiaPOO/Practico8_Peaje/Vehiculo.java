package EjerciciosGuiaPOO.Practico8_Peaje;

public class Vehiculo {
    private String placa;
    private String tipo;

    public Vehiculo(String placa, String tipo) {
        this.placa = placa;
        this.tipo = tipo;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
